class Solution {
  public:
    // Function returns the second
    // largest elements
    int getSecondLargest(vector<int> &arr) {
        // Code Here
        priority_queue<int>q;
        unordered_set<int>s;
        for(int&num:arr){
            if(s.find(num)==s.end()){
              q.push(num); 
              s.insert(num);
            }
        }
        int maxN=q.top();
        q.pop();
        return q.top()!=maxN?q.top():-1;
    }
};

